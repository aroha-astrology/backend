/**
 * `device_push_tokens.locale` is free-form (whatever the OS/browser reports,
 * e.g. `hi-IN`, `en-US`, or null) — normalize to our 7 supported codes,
 * falling back to English for anything else so a push is never skipped for
 * an unrecognized locale.
 */
export function normalizeLang(locale: string | null | undefined): LangCode {
  if (!locale) return 'en';
  const base = locale.split('-')[0]?.toLowerCase();
  return (SUPPORTED_LANGS as readonly string[]).includes(base ?? '') ? (base as LangCode) : 'en';
}

/** `dayOfWeek`: 0 (Sunday) – 6 (Saturday), IST. */
export function getDailyCopy(lang: LangCode, dayOfWeek: number): PushCopy {
  const hooks = DAILY_COPY[lang] ?? DAILY_COPY.en;
  return hooks[dayOfWeek] ?? hooks[0]!;
}

export function getPeriodicCopy(
  period: Exclude<BroadcastPeriod, 'daily'>,
  lang: LangCode,
): PushCopy {
  const byLang = PERIODIC_COPY[period];
  return byLang[lang] ?? byLang.en;
}
