function ReportPdf({
  reportType,
  client,
  branding,
  content,
}: {
  reportType: string;
  client: Record<string, string | null>;
  branding: Record<string, string | null>;
  content: string;
}) {
  const accentColor = branding.primary_color ?? '#7c3aed';
  return (
    React.createElement(Document, null,
      React.createElement(Page, { size: 'A4', style: styles.page },
        // Header
        React.createElement(View, { style: styles.header },
          branding.logo_url
            ? React.createElement(Image, { src: branding.logo_url, style: styles.logo })
            : React.createElement(View, { style: { ...styles.logo, backgroundColor: accentColor, justifyContent: 'center', alignItems: 'center' } },
                React.createElement(Text, { style: { color: '#fff', fontSize: 20 } }, '🔮')
              ),
          React.createElement(View, null,
            React.createElement(Text, { style: { ...styles.brandName, color: accentColor } }, branding.brand_name ?? 'Astrologer Portal'),
            branding.tagline ? React.createElement(Text, { style: styles.tagline }, branding.tagline) : null,
          )
        ),
        // Report title
        React.createElement(View, { style: styles.section },
          React.createElement(Text, { style: { ...styles.title, color: accentColor } }, `${REPORT_TITLES[reportType] ?? reportType} Report`),
          React.createElement(Text, { style: styles.subtitle }, `Prepared for: ${client.name ?? 'Client'}`),
        ),
        // Client info
        React.createElement(View, { style: styles.section },
          React.createElement(Text, { style: styles.sectionTitle }, 'Birth Details'),
          React.createElement(View, { style: styles.row }, React.createElement(Text, { style: styles.label }, 'Name'), React.createElement(Text, { style: styles.value }, client.name ?? '—')),
          React.createElement(View, { style: styles.row }, React.createElement(Text, { style: styles.label }, 'Date of Birth'), React.createElement(Text, { style: styles.value }, client.dob ?? '—')),
          React.createElement(View, { style: styles.row }, React.createElement(Text, { style: styles.label }, 'Time of Birth'), React.createElement(Text, { style: styles.value }, client.birth_time ?? '—')),
          React.createElement(View, { style: styles.row }, React.createElement(Text, { style: styles.label }, 'Place of Birth'), React.createElement(Text, { style: styles.value }, client.birth_place ?? '—')),
        ),
        // Content
        React.createElement(View, { style: styles.section },
          React.createElement(Text, { style: styles.sectionTitle }, 'Analysis'),
          React.createElement(Text, { style: styles.body }, content),
        ),
        // Footer
        React.createElement(View, { style: styles.footer, fixed: true },
          React.createElement(Text, { style: styles.footerText }, branding.pdf_footer ?? `Generated by ${branding.brand_name ?? 'Astrologer Portal'}`),
          React.createElement(Text, { style: styles.footerText }, new Date().toLocaleDateString('en-IN')),
        )
      )
    )
  );
}

