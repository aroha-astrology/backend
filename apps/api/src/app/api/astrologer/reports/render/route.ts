export async function POST(req: NextRequest) {
  const supabase = await createServerSupabase();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

  const { report_type, customer_id } = await req.json() as { report_type: string; customer_id: string };

  if (!report_type || !customer_id) {
    return NextResponse.json({ error: 'report_type and customer_id required' }, { status: 400 });
  }

  const [{ data: client }, { data: brandingRow }] = await Promise.all([
    supabase
      .from('astrologer_customers')
      .select('name, dob, birth_time, birth_place')
      .eq('id', customer_id)
      .eq('astrologer_id', user.id)
      .maybeSingle(),
    supabase
      .from('astrologer_branding')
      .select('brand_name, logo_url, tagline, primary_color, pdf_footer')
      .eq('astrologer_id', user.id)
      .maybeSingle(),
  ]);

  if (!client) return NextResponse.json({ error: 'Client not found' }, { status: 404 });

  const clientData: Record<string, string | null> = {
    name:        client.name ?? null,
    dob:         client.dob  ?? null,
    birth_time:  (client as Record<string, unknown>).birth_time as string | null ?? null,
    birth_place: (client as Record<string, unknown>).birth_place as string | null ?? null,
  };

  const branding: Record<string, string | null> = {
    brand_name:    brandingRow?.brand_name    ?? null,
    logo_url:      brandingRow?.logo_url      ?? null,
    tagline:       brandingRow?.tagline       ?? null,
    primary_color: brandingRow?.primary_color ?? null,
    pdf_footer:    brandingRow?.pdf_footer    ?? null,
  };

  const contentFn = REPORT_CONTENT[report_type];
  if (!contentFn) return NextResponse.json({ error: 'Unknown report type' }, { status: 400 });

  const content = contentFn(clientData);

  const docElement = React.createElement(ReportPdf, { reportType: report_type, client: clientData, branding, content });
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const pdfBuffer = await renderToBuffer(docElement as any);

  return new NextResponse(pdfBuffer as unknown as BodyInit, {
    headers: {
      'Content-Type':        'application/pdf',
      'Content-Disposition': `attachment; filename="${report_type}-report-${clientData.name?.replace(/\s+/g, '-') ?? 'client'}.pdf"`,
    },
  });
}
